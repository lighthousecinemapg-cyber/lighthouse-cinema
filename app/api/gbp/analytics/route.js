// GET â full analytics dashboard data
export async function GET() {
  const reviewStats = getReviewStats();
  const postStats = getPostStats();
  const photos = getPhotoUploads(10);
  const history = getAnalyticsHistory(30);

  const dashboard = {
    overview: {
      totalReviews: reviewStats.total,
      avgRating: reviewStats.avgRating,
      escalatedReviews: reviewStats.escalated,
      totalPostsPublished: postStats.totalPublished,
      totalPostsFailed: postStats.totalFailed,
      totalImpressions: postStats.totalImpressions,
      totalClicks: postStats.totalClicks,
      photosUploaded: photos.length,
    },
    reviewBreakdown: reviewStats.byCategory,
    postBreakdown: postStats.byType,
    recentPhotos: photos,
    history,
    generatedAt: new Date().toISOString(),
  };

  return NextResponse.json(dashboard);
}

// POST â generate and send weekly report
export async function POST(request) {
  try {
    const reviewStats = getReviewStats();
    const postStats = getPostStats();

    const metrics = {
      newReviews: reviewStats.total,
      avgRating: reviewStats.avgRating,
      avgResponseTime: 12, // minutes â tracked in production
      postsPublished: postStats.totalPublished,
      totalImpressions: postStats.totalImpressions,
      ctaClicks: postStats.totalClicks,
      ticketsFromGBP: 0, // tracked via UTM in production
      photoViews: 0, // from GBP insights API
    };

    // Generate AI report
    const report = await generateWeeklyReport(metrics);

    // Save snapshot
    saveAnalyticsSnapshot({
      ...metrics,
      aiGrade: report.grade,
      aiSummary: report.summary,
    });

    // Email the report
    const managerEmail = process.env.MANAGER_EMAIL || 'lighthousecinemapg@gmail.com';
    try {
      await sendWeeklyReportEmail(managerEmail, report, metrics);
    } catch (emailErr) {
      console.error('Failed to email weekly report:', emailErr.message);
    }

    return NextResponse.json({
      success: true,
      report,
      metrics,
    });

  } catch (err) {
    console.error('Analytics report error:', err);
    return NextResponse.json({ error: 'Report generation failed' }, { status: 500 });
  }
}

