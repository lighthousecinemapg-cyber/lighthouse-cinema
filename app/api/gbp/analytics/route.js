import { NextResponse } from 'next/server';
import { getReviewStats, getPostStats, getPhotoUploads, getAnalyticsHistory, saveAnalyticsSnapshot } from '@/lib/gbp/automation-db';
import { generateWeeklyReport } from '@/lib/gbp/ai-engine';
